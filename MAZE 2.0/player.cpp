int checkCollision(Player* player, float newX, float newY) {
    // Convert the new position to map coordinates
    int mapX = (int)newX;
    int mapY = (int)newY;

    // Check if the new position is within the map bounds
    if (mapX < 0 || mapX >= mapWidth || mapY < 0 || mapY >= mapHeight) {
        return 1; // Collision (out of bounds)
    }

    // Check if the new position is a wall
    if (worldMap[mapX][mapY] > 0) {
        return 1; // Collision (wall)
    }

    return 0; // No collision
}

void movePlayer(Player* player, float deltaTime) {
    // Calculate movement distance based on time and speed
    float moveStep = player->moveSpeed * deltaTime;

    // Calculate new position based on player direction
    float newX = player->x + cos(player->angle * M_PI / 180.0f) * moveStep;
    float newY = player->y + sin(player->angle * M_PI / 180.0f) * moveStep;

    // Check for collision before updating the player's position
    if (!checkCollision(player, newX, newY)) {
        player->x = newX;
        player->y = newY;
    }
}